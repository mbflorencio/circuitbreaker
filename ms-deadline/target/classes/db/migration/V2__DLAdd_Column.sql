INSERT INTO deadline (client, deadline)
SELECT
    ROWNUM(),
    CASE
        WHEN ROWNUM() BETWEEN 1 AND 5 THEN 10
        WHEN ROWNUM() BETWEEN 6 AND 10 THEN 20
        WHEN ROWNUM() BETWEEN 11 AND 15 THEN 30
        ELSE 40
        END
FROM
    (SELECT 1 FROM SYSTEM_RANGE(1, 20))
ORDER BY RANDOM();